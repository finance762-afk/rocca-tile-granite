            <div class="prose">
              <p>Proper subfloor preparation is what separates a flooring installation that lasts from one that buckles, cracks, or pops loose within a year. Before any new material goes down, we check for moisture levels, subfloor levelness, and structural integrity. High moisture means a vapor barrier. Uneven areas get leveled with self-leveling compound. Damaged sections of subfloor get replaced — not patched over and hidden. We don't skip this step because the consequences show up in six months, and by then the fix costs twice as much.</p>
              <p style="margin-top:var(--space-md);">Once the subfloor is sound, the installation itself follows manufacturer specifications for the chosen material — proper adhesive or fastener systems, correct expansion gaps, appropriate underlayment, and precise cuts at walls, doorways, and fixtures. We handle transitions between rooms where flooring types or heights change, install new baseboards or quarter-round trim, and finish thresholds at exterior doors. Every detail that touches the floor gets addressed — not just the flooring itself.</p>
